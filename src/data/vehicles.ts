export function getVehicleBySlug(slug: string): Vehicle | undefined {
  return vehicles.find((v) => v.slug === slug);
}

export function getFeaturedVehicles(): Vehicle[] {
  return vehicles.filter((v) => v.featured && v.available);
}

export function getVehiclesByCategory(category: string): Vehicle[] {
  return vehicles.filter(
    (v) => v.category === category && v.available
  );
}

export function getAllBrands(): string[] {
  return [...new Set(vehicles.map((v) => v.brand))].sort();
}

export function getAllCategories(): string[] {
  return [...new Set(vehicles.map((v) => v.category))];
}
